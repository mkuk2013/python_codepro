# Task : 1 Compute the sum, difference, and product of two numbers.

a = 25
b = 15

# Sum of two numbers
result = a + b
print(f'Sum of {a} and {b} is equal to {result}')

# Difference of two numbers
result = a - b
print(f'Difference of {a} and {b} is equal to {result}')

# Product of two numbers
result = a * b
print(f'Product of {a} and {b} is equal to {result}')
